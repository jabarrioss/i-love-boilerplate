    -- custom fields here
    return self
end

function %s:update(dt)
    %s.super.update(self, dt)
    -- custom logic here
end

function %s:draw()
    %s.super.draw(self)
    -- custom rendering here
end

return %s
]]):format(name, name, name, name, name, name, name, name, name, name, name)
end

