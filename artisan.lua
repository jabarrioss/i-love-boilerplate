--[[
    artisan.lua — Laravel-style CLI for the Love2D boilerplate.

    Run from the project root:

        lua artisan.lua list
        lua artisan.lua make:scene InventoryScene
        lua artisan.lua make:entity Bullet
        lua artisan.lua make:service ScoreManager
        lua artisan.lua make:test test_input
        lua artisan.lua make:ui HealthBar
        lua artisan.lua make:config gameplay

    No LÖVE required — pure Lua so it works in CI, scripts, and editors.
]]

local commands = {}
local args = { ... }

-- ---------------------------------------------------------------------------
-- Utilities
-- ---------------------------------------------------------------------------
local function writeFile(path, content)
    local file, err = io.open(path, "w")
    if not file then
        print(("  [error] could not open %s: %s"):format(path, err))
        return false
    end
    file:write(content)
    file:close()
    print(("  [created] %s"):format(path))
    return true
end

local function fileExists(path)
    local f = io.open(path, "r")
    if f then f:close(); return true end
    return false
end

-- ---------------------------------------------------------------------------
-- Template generators
-- ---------------------------------------------------------------------------
local function sceneTemplate(name)
    return ([[
-- %s — scene stub
-- Generated by artisan make:scene. Edit me.
local Scene = require("core.Scene")

local %s = Scene:extend("%s")

function %s:enter()
    -- called once when this scene becomes active
end

function %s:exit()
    -- called once when leaving this scene
end

function %s:update(dt)
    -- game logic, called every frame
end

function %s:draw()
    -- rendering, called every frame
end

function %s:keypressed(key, scancode, isrepeat) end
function %s:mousepressed(x, y, button) end

return %s
]]):format(name, name, name, name, name, name, name, name, name, name)
end

