-- ---------------------------------------------------------------------------
-- Dispatcher
-- ---------------------------------------------------------------------------
local cmd = args[1]
local arg1 = args[2]

if not cmd or cmd == "list" or cmd == "help" then
    commands.list()
    print("")
    print("Run from project root, e.g.:")
    print("  lua artisan.lua make:scene InventoryScene")
    os.exit(0)
end

local makeType = cmd:match("^make:(.+)$")
if not makeType then
    print("Unknown command: " .. tostring(cmd))
    print("Run 'lua artisan.lua list' to see available commands.")
    os.exit(1)
end

local handler = commands[makeType]
if not handler then
    print("Unknown make type: " .. makeType)
    os.exit(1)
end

handler(arg1)
