local function configTemplate(name)
    return ([[
-- config/%s.lua — configuration stub
return {
    -- Add your config keys here.
}
]]):format(name)
end

-- ---------------------------------------------------------------------------
-- Command registry
-- ---------------------------------------------------------------------------
commands.list = function()
    print("Available commands:")
    for name, _ in pairs(commands) do
        if name ~= "list" then
            print("  make:" .. name)
        end
    end
end

commands.scene = function(name)
    if not name then return print("Usage: artisan make:scene <Name>") end
    local path = "scenes/" .. name .. ".lua"
    if fileExists(path) then return print("  [skip] " .. path .. " already exists") end
    writeFile(path, sceneTemplate(name))
end

commands.entity = function(name)
    if not name then return print("Usage: artisan make:entity <Name>") end
